    private void btAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAtualizarActionPerformed
        // TODO add your handling code here:
       
        int id = Integer.parseInt(txtIDESP.getText());
        String nome = txtNomeESP.getText();
        String classificacao = txtClassESP.getText();
        String conservacao = txtConsESP.getText();

        Especies e = new Especies();
        e.setId(id);
        e.setNome(nome);
        e.setClassificacao(classificacao);
        e.setConservacao(conservacao);

        EspeciesDAO especiesDAO = new EspeciesDAO();
        EspeciesDAO.alterar(e);

        txtIDESP.setText("");
        txtNomeESP.setText("");
        txtClassESP.setText("");
        txtConsESP.setText("");
    }//GEN-LAST:event_btAtualizarActionPerformed

