    private void btCriarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCriarActionPerformed
        // TODO add your handling code here:
        String nome = txtNomeESP.getText();
        String classificacao = txtClassESP.getText();
        String conservacao = txtConsESP.getText();
        
        Especies e = new Especies();
        e.setNome(nome);
        e.setClassificacao(classificacao);
        e.setConservacao(conservacao);
        
        EspeciesDAO especiesDAO = new EspeciesDAO();
        EspeciesDAO.inserir(e);
        
        txtNomeESP.setText("");
        txtClassESP.setText("");
        txtConsESP.setText("");
        
    }//GEN-LAST:event_btCriarActionPerformed

    private void btEncontrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEncontrarActionPerformed
        // TODO add your handling code here:
       
        int idPesquisa = Integer.parseInt(txtIDPesquisa.getText());
        EspeciesDAO especiesDAO = new EspeciesDAO();
        Especies e = especiesDAO.getEspecies(idPesquisa);
        if(e == null) {
            JOptionPane.showMessageDialog(this, "Produto não encontrado");
        } else {
            txtIDESP.setText(String.valueOf(e.getId()));
            txtNomeESP.setText(String.valueOf(e.getNome()));
            txtClassESP.setText(String.valueOf(e.getClassificacao()));
            txtConsESP.setText(String.valueOf(e.getConservacao()));
        }
        
    }//GEN-LAST:event_btEncontrarActionPerformed

